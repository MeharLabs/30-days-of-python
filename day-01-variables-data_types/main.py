# Data Types

# int
age = 24
number = 5

# float
price = 19.99
quantity = 29.09

# str
greeting = "Hi"
name = "Red John"

# str - blank
name = ""

# str - Empty Space
name = " "

# bool
online = True
online = False

# list
tasks = ["wake up", "eat", "code", "sleep"]
marks = [85, 90, 78, 92]

# tuple
days = ("Mon", "Tue", "Wed", "Thu", "Fri")
red = (255, 0, 0)

# dict
user = {
    "name": "John",
    "age": 24,
    "vibe": "coding"
}